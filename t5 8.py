'''
Напишіть програму, яка зчитує ціле число і виводить текст, 
аналогічний наведеному в прикладі (прогалини і точки важливі!).

#TODO
output:
Після числа 5 йде число 6.
Перед числом 5 йде число 4.

'''
# Запит користувача для введення цілого числа
number = int(input("Введіть ціле число: "))

# Виведення тексту залежно від введеного числа
print("Після числа", number, "йде число", number + 1, ".")
print("Перед числом", number, "йде число", number - 1, ".")
