'''
Програма повинна обчислювати зріст користувача в папугах. 
Чому не виходить? 
Згадайте, навіщо потрібні функції int () і str ().
'''

r = input("Який у тебе зріст (сантиметрів)?")
rost_p = 17
p = int(r) // rost_p  # Використовуємо int(), щоб конвертувати введене значення у ціле число
answer = "Це " + str(p) + " папуг!"  # Використовуємо str(), щоб конвертувати ціле число у рядок
print(answer)
