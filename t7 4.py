'''
Квадрат числа
який буде результат, якщо будь яке число помножити саме на себе?
1) Програма запитує будь яке число, перемножує, це число, саме на себе, а потім друкує:
"Результат = <результат множення>".
'''
# Запит користувача для введення числа
number = float(input("Введіть будь-яке число: "))

# Обчисліть квадрат числа
result = number * number

# Виведіть результат
print("Результат =", result)
